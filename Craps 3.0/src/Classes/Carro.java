public class Carro {

    private String cod_carro;
    private String placa;
    private String modelo;
    private String trancacao;
    private String valor;
    private String obs_financeiro;
    private String obs;
    private String status;

    /**
     * @return the placa
     */
    public String getPlaca() {
        return placa;
    }

    /**
     * @param placa the placa to set
     */
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    /**
     * @return the nome
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param nome the nome to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the valor
     */
    public String getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(String valor) {
        this.valor = valor;
    }

    /**
     * @return the obs
     */
    public String getObs() {
        return obs;
    }

    /**
     * @param obs the obs to set
     */
    public void setObs(String obs) {
        this.obs = obs;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the cod_carro
     */
    public String getCod_carro() {
        return cod_carro;
    }

    /**
     * @param cod_carro the cod_carro to set
     */
    public void setCod_carro(String cod_carro) {
        this.cod_carro = cod_carro;
    }

    /**
     * @return the trancacao
     */
    public String getTrancacao() {
        return trancacao;
    }

    /**
     * @param trancacao the trancacao to set
     */
    public void setTrancacao(String trancacao) {
        this.trancacao = trancacao;
    }

    /**
     * @return the obs_financeiro
     */
    public String getObs_financeiro() {
        return obs_financeiro;
    }

    /**
     * @param obs_financeiro the obs_financeiro to set
     */
    public void setObs_financeiro(String obs_financeiro) {
        this.obs_financeiro = obs_financeiro;
    }

}
