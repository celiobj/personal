package Classes;

/**
 *
 * @author celio
 */
public class Dia {
    
    private String codDia;
    private String saldo_anterior;
    private String dia_mes;
    private String saldo_atual;
    private String situacao;

    /**
     * @return the codDia
     */
    public String getCodDia() {
        return codDia;
    }

    /**
     * @param codDia the codDia to set
     */
    public void setCodDia(String codDia) {
        this.codDia = codDia;
    }

    /**
     * @return the saldo_anterior
     */
    public String getSaldo_anterior() {
        return saldo_anterior;
    }

    /**
     * @param saldo_anterior the saldo_anterior to set
     */
    public void setSaldo_anterior(String saldo_anterior) {
        this.saldo_anterior = saldo_anterior;
    }

    /**
     * @return the dia_mes
     */
    public String getDia_mes() {
        return dia_mes;
    }

    /**
     * @param dia_mes the dia_mes to set
     */
    public void setDia_mes(String dia_mes) {
        this.dia_mes = dia_mes;
    }

    /**
     * @return the saldo_atual
     */
    public String getSaldo_atual() {
        return saldo_atual;
    }

    /**
     * @param saldo_atual the saldo_atual to set
     */
    public void setSaldo_atual(String saldo_atual) {
        this.saldo_atual = saldo_atual;
    }

    /**
     * @return the situacao
     */
    public String getSituacao() {
        return situacao;
    }

    /**
     * @param situacao the situacao to set
     */
    public void setSituacao(String situacao) {
        this.situacao = situacao;
    }
    
}
